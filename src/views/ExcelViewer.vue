import BarChart from "../components/BarChart.vue";

export default {
  name: "ExcelViewer",
  components: {
    BarChart,
  },
  data() {
    return {
      graph: false,
      URL: `https://docs.google.com/spreadsheets/d/${process.env.VUE_APP_SHEET_ID}/gviz/tq?sheet=${process.env.VUE_APP_SHEET_TITLE}&range=${process.env.VUE_APP_SHEET_RANGE}`,
      cols: "",
      rows: "",
      chartData: {
        labels: [],
        datasets: [
          {
            label: "Fossil Fuel Pollution",
            data: [],
            backgroundColor: ["rgba(54, 162, 235, 1)"],
          },
        ],
      },
      chartOptions: {
        maintainAspectRatio: false,
        responsive: true,
      },
    };
  },
  methods: {
    ...mapActions(["fetchData"]),
    graphToggle() {
      this.graph = !this.graph;
    },
  },
  computed: {
    ...mapGetters(["getExcelData"]),
  },
  async created() {
    await this.fetchData(this.URL);
    this.cols = this.getExcelData.cols;
    this.rows = this.getExcelData.rows;

    // Setting X Labels
    this.chartData.labels = this.cols.filter(
      (label) => label !== "" && typeof label === "number"
    );

    // Setting Y Data
    this.chartData.datasets[0].data = this.rows
      .slice(0, 8)
      .filter((datum) => datum !== "" && typeof datum === "number");
  },
};
</script>
